package com.example.chinmayagroupchatandmemeapp;

public class ImgPojo {
    private String id;
    private String url;
    private String post;


    // Getter Methods

    public String getId() {
        return id;
    }

    public String getUrl() {
        return url;
    }

    public String getPost() {
        return post;
    }

    // Setter Methods

    public void setId(String id) {
        this.id = id;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public void setPost(String post) {
        this.post = post;
    }
}
